package introexceptiontrycatchtrace;

public class Calculator {

    public int  addNumbers(int number, int anotherNumber){
        return number+anotherNumber;
    }

    public int  extractionNumbers(int number, int anotherNumber){
        return number-anotherNumber;
    }

    public int  divideNumbers(int number, int anotherNumber){
        return number/anotherNumber;
    }

    public int  multipleNumbers(int number, int anotherNumber){
        return number*anotherNumber;
    }

}
